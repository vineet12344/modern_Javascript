var num1 = 10;
var num2 = 88.4;

// toString()   ---> This function return string format of any value the varible is holding

console.log(num1.toString());
console.log(num2.toString());

// parseInt()     ---> converts string numerals to interger type

var string1 = "15";
var string2 = "55.5";

console.log(parseInt(string1));
console.log(parseInt(string2));

// parseFloat()   ---> convert to float 

var string3 = "15.7";
var string4 = "78.5";

console.log(parseFloat(string3));
console.log(parseFloat(string4));


// toFixed(roundoff_value)   ---> takes floating number and round it off to given parameter 

var strFloat = 25.8456123;

console.log(strFloat.toFixed(5))

